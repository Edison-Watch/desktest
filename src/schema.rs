/// Write the schema to a file path, returning the schema JSON for display.
pub fn write_schema(output: &std::path::Path) -> Result<(), crate::error::AppError> {
    let schema = generate_task_schema();
    let pretty = serde_json::to_string_pretty(&schema)
        .map_err(|e| crate::error::AppError::Config(format!("Failed to serialize schema: {e}")))?;
    std::fs::write(output, format!("{pretty}\n")).map_err(|e| {
        crate::error::AppError::Config(format!("Failed to write {}: {e}", output.display()))
    })?;
    Ok(())
}

use serde_json::{Value, json};

/// Generate a JSON Schema for the task definition format.
///
/// This is hand-written rather than derived because:
/// 1. schemars adds a heavy dependency
/// 2. We need to match serde's `#[serde(tag = "type")]` discriminator pattern exactly
/// 3. We want human-friendly descriptions inline
pub fn generate_task_schema() -> Value {
    json!({
        "$schema": "https://json-schema.org/draft/2020-12/schema",
        "$id": "https://desktest.dev/schemas/task-v1.0.json",
        "title": "Desktest Task Definition",
        "description": "Schema for desktest task JSON files (schema_version 1.0)",
        "type": "object",
        "required": ["schema_version", "id", "instruction", "app"],
        "additionalProperties": false,
        "properties": {
            "schema_version": {
                "type": "string",
                "const": "1.0",
                "description": "Schema version. Must be \"1.0\"."
            },
            "id": {
                "type": "string",
                "minLength": 1,
                "description": "Unique identifier for this task."
            },
            "instruction": {
                "type": "string",
                "minLength": 1,
                "description": "Natural language instruction for the agent — what it should do."
            },
            "completion_condition": {
                "type": "string",
                "description": "Optional description of when the agent should consider the task done."
            },
            "app": { "$ref": "#/$defs/AppConfig" },
            "config": {
                "type": "array",
                "items": { "$ref": "#/$defs/SetupStep" },
                "default": [],
                "description": "Setup steps to execute before the agent loop."
            },
            "evaluator": { "$ref": "#/$defs/EvaluatorConfig" },
            "timeout": {
                "type": "integer",
                "minimum": 1,
                "default": 300,
                "description": "Overall test timeout in seconds."
            },
            "max_steps": {
                "type": "integer",
                "minimum": 1,
                "default": 15,
                "description": "Maximum steps for the agent loop."
            },
            "a11y_timeout_secs": {
                "type": "integer",
                "minimum": 1,
                "description": "Accessibility tree extraction timeout in seconds. Skips adaptive probe if set."
            },
            "max_a11y_nodes": {
                "type": "integer",
                "minimum": 1,
                "default": 10000,
                "description": "Maximum number of accessibility tree nodes to extract."
            },
            "secrets": {
                "type": "object",
                "additionalProperties": { "$ref": "#/$defs/SecretDef" },
                "description": "Named secrets sourced from environment variables. Keys are placeholder names for {{key}} substitution."
            },
            "metadata": {
                "description": "Arbitrary metadata (tags, author, description, etc.)."
            },
            "replay_script": {
                "type": "string",
                "description": "Path to a codified Python replay script (generated by `desktest codify`)."
            },
            "replay_screenshots_dir": {
                "type": "string",
                "description": "Directory containing expected screenshots for replay visual assertions."
            },
            "early_exit": { "$ref": "#/$defs/EarlyExitConfig" }
        },
        "$defs": {
            "SecretDef": secret_def_schema(),
            "AppConfig": app_config_schema(),
            "SetupStep": setup_step_schema(),
            "EvaluatorConfig": evaluator_config_schema(),
            "MetricConfig": metric_config_schema(),
            "EarlyExitConfig": early_exit_config_schema(),
            "EarlyExitCondition": early_exit_condition_schema()
        }
    })
}

fn secret_def_schema() -> Value {
    json!({
        "type": "object",
        "required": ["env"],
        "additionalProperties": false,
        "properties": {
            "env": {
                "type": "string",
                "description": "Environment variable name to read the secret value from."
            },
            "default": {
                "type": "string",
                "description": "Optional default value when the environment variable is not set."
            }
        }
    })
}

